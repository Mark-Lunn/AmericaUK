                    <p><strong>Little Big Town </strong>first entered the music scene 15 years ago with hits such as &quot;Boondocks,&quot; &quot;Bring It On Home,&quot; &quot;Good As Gone&quot; and Grammy-nominated single &quot;Little White Church.&quot;
                        <!-- o ignored -->
                    </p>
                    <p>
                      <!-- o ignored -->
                       
                    </p>
                    <p>Their highly-anticipated sixth studio album <em>Pain Killer</em> which has already been released in the US to rave critical review and features #1 single &quot;Day Drinking&quot;.  <em>Pain Killer </em>produced by Jay Joyce follows the bands platinum-selling album <em>Tornado </em>which includes #1 hits &quot;Pontoon&quot; and &quot;Tornado.&quot;
                        <!-- o ignored -->
                    </p>
                    <ul>
                      <li>·         Little Big Town won the CMA's Vocal Group of the Year award for the second year running in November 2014.
                          <!-- o ignored -->
                      </li>
                      <li>·         Little Big Town performed on the 2014 CMA Award show with Ariana Grande.
                          <!-- o ignored -->
                      </li>
                      <li>·         Little Big Town are now on the BBC Radio 2 'B' playlist with their Grammy nominated, American smash hit single 'Day Drinking'
                          <!-- o ignored -->
                      </li>
                      <li>·         The new Little Big Town album <em><U>Pain Killer</U></em> is Billboard's #2 Country Album of 2015, Rolling Stone Country #5 album of 2014
                          <!-- o ignored -->
                      </li>
                      <li>·         Little Big Town made their UK debut at The O2, London in 2013 as part of the very first Country to Country Festival where they received a fantastic reception for their high energy live show featuring their biggest hits and arresting covers of songs like Fleetwood Mac's The Chain
                          <!-- o ignored -->
                      </li>
                    </ul>
                    <p>
                      <!-- o ignored -->
                       
                    </p>
                    <p><strong><span lang="en" xml:lang="en">BIOGRAPHY
                            <!-- o ignored -->
                    </span></strong></p>
                    <p><span lang="en" xml:lang="en">Hot on the heels of their wildly successful album </span><em><span lang="en" xml:lang="en">Tornado, </span></em><span lang="en" xml:lang="en">Little Big Town's prescription for continued success and creative drive is in their sixth album, </span><em><span lang="en" xml:lang="en">Pain Killer. </span></em><span lang="en" xml:lang="en">The two years since </span><em><span lang="en" xml:lang="en">Tornado's </span></em><span lang="en" xml:lang="en">release have proven to be the most formative and motivational for the inspired family of artists, together since 1998.
                      <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">In the short time since </span><em><span lang="en" xml:lang="en">Tornado</span></em><span lang="en" xml:lang="en">, Little Big Town earned two number one radio hits with </span><em><span lang="en" xml:lang="en">Tornado </span></em><span lang="en" xml:lang="en">and </span><em><span lang="en" xml:lang="en">Pontoon</span></em><span lang="en" xml:lang="en">, a Grammy, two ACM and three CMA awards and an Emmy. They also found time to headline a sold-out tour, join Keith Urban across North America and host the CMA Music Festival: Country's Night to Rock twice.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">These hard-earned accolades and new opportunities provided a steady dose of inspiration at every turn, leading them to a fresh and very intentional approach to their latest studio album. They co-wrote most of </span><em><span lang="en" xml:lang="en">Tornado's</span></em><span lang="en" xml:lang="en"> songs as a group and knocked out production in a matter of weeks, whereas the road to </span><em><span lang="en" xml:lang="en">Pain Killer </span></em><span lang="en" xml:lang="en">was significantly longer in the making, and much more calculated.
                      <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">LBT intentionally began writing and curating their songs early in their tour for </span><em><span lang="en" xml:lang="en">Tornado </span></em><span lang="en" xml:lang="en">in May 2013. They followed the creative energy wherever it flowed by splitting into different writing combinations.  &quot;We decided not to lock ourselves into writing as a group. We wanted a more relaxed and free approach,&quot; says Karen Fairchild. &quot;There was no pressure to write as certain groups at certain times. We followed the inspiration instead of forcing it.&quot;
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">&quot;I don't know that we would have written </span><em><span lang="en" xml:lang="en">Tumble and Fall </span></em><span lang="en" xml:lang="en">if the boys had been in the room,&quot; she continues. &quot;The writing process on that song was very therapeutic for all of us girls. Just as </span><em><span lang="en" xml:lang="en">Faster Gun </span></em><span lang="en" xml:lang="en">is a guy's song, it probably wouldn't have turned out the same way had the girls been in the room.&quot;
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">New voices, including Ryan Tyndell, Blair Daly, Jeremy Spillman and Shane McAnally, joined long-term LBT collaborators, such as Hillary Lindsey, Liz Rose, Lori McKenna, Jedd Hughes and Natalie Hemby.  As a result, </span><em><span lang="en" xml:lang="en">Pain Killer</span></em><span lang="en" xml:lang="en"> covers all new territory for LBT. The band and its writing and production partners favored multi-layered effects. They drew from amix of influences including vintage 50's country, 70's country, funk, groove, a cappella, bluegrass and straight up rock n' roll.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">This evolution of LBT's sound is the outcome of their free reign to write and craft as they chose, making </span><em><span lang="en" xml:lang="en">Pain Killer</span></em><span lang="en" xml:lang="en"> as uninhibited as their creative process.  &quot;We don't think about boundaries anymore. We let go of that because it doesn't work for us. We do better when we're freed up,&quot; Karen says.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">Phillip Sweet offers an enlightened perspective on songwriting. &quot;You chase whatever idea starts the creative process. It might be a lyric. It might be a melody. Sometimes a song unloads on you and you have to catch it and hang on for dear life. [Writing </span><em><span lang="en" xml:lang="en">Pain Killer</span></em><span lang="en" xml:lang="en">] was a healthy competition and motivating. The best songs won. There was no ego involved in that.&quot;    </span><em><span lang="en" xml:lang="en">Pain Killer </span></em><span lang="en" xml:lang="en">proves LBT has a strong command on the courage it takes to create. &quot;We have learned to trust ourselves. It's confidence and experience. We're braver than we've ever been on this record,&quot; explains Kimberly Schlapman.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">LBT recorded 23 songs for </span><em><span lang="en" xml:lang="en">Pain Killer, </span></em><span lang="en" xml:lang="en">ultimately narrowing the album to 13. &quot;The creative process is such a living thing,&quot; says Jimi Westbook. &quot;We've become good at acknowledging when it's not working. It's easy to try to force it, but we've grown to understand when to move on. There came a point when the song selection came together and felt right. It had a great personality.&quot;
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">The bonds LBT and producer, songwriter and musician extraordinaire Jay Joyce formed when producing </span><em><span lang="en" xml:lang="en">Tornado </span></em><span lang="en" xml:lang="en">carried over seamlessly into the creation of </span><em><span lang="en" xml:lang="en">Pain Killer. </span></em><span lang="en" xml:lang="en">This relationship, combined with the unstructured writing process and the use of their road band in the studio, gave way to a new adventure in experimenting with sound.
                      <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">Jimi makes an astute observation of Joyce, &quot;He is such an amazing, creative person and fun to work with. He takes you places you don't expect to go; and that's exciting, musically. You feel a lot of freedom in that.&quot;
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">&quot;Jay is like a mad scientist. He uses our voices as instruments. Literally!&quot; Phillip exclaims. &quot;It was a deliberate choice to use our voices in ways we hadn't before. It was exhilarating.&quot;   Today's recording standards are streamlined and corrected, manipulated and often times over polished. Joyce makes music very differently, as found throughout critically acclaimed partnerships with artists such as Cage the Elephant, Amos Lee, Eric Church and Emmylou Harris.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">&quot;Jay doesn't believe in a cleaned up, pristine track,&quot; adds Karen. &quot;Sometimes you don't even know what layers exist. He will wake up in the middle of the night and go lay down some great, totally unexpected elements.&quot;
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">Kimberly also enjoys Joyce's creative drive. &quot;He is very spontaneous when recording. He leaves a lot in. That's good for us!&quot;  One sign of a successful collaboration is simple: LBT still listens to </span><em><span lang="en" xml:lang="en">Pain Killer </span></em><span lang="en" xml:lang="en">and hears sounds and effects they never noticed before, an experience musical craftsmen the world over are sure to envy.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">&quot;</span><em><span lang="en" xml:lang="en">Pain Killer</span></em><span lang="en" xml:lang="en"> has a lot of different sounds without sounding unorganized,&quot; says Joyce. &quot;It's a more artistic album than LBT has done before. It has a lot of integrity.&quot;   The provocative album has something for everyone: A treatment for the broken heart or the shattered spirit, a rally cry for those exhausted by love yet still inspired by it, a testament to the enduring hope of a long relationship, a promise of perseverance and a shot of good, old-fashioned fun.  </span><em><span lang="en" xml:lang="en">Pain Killer </span></em><span lang="en" xml:lang="en">leads off with &quot;Quit Breaking Up with Me,&quot; a power pop anthem for those infamous on-again / off-again relationships that are plagued with drama and indecision. &quot;It has so much attitude!&quot; says Jimi. Written by Busbee, Natalie Hemby and Shane McAnally, it's laced with punk, a shot of rock and rolls with LBT's characteristic country sass.
                      <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">&quot;Day Drinking&quot;was the first song LBT wrote as a group for </span><em><span lang="en" xml:lang="en">Pain Killer, </span></em><span lang="en" xml:lang="en">along with Troy Verges and Barry Dean, and is the album's first single</span><em><span lang="en" xml:lang="en">. </span></em><span lang="en" xml:lang="en">Its fanciful marching band and quirky whistles work together brilliantly to create a playful song of summer. &quot;People are genuinely happy when they hear it,&quot; adds Phillip. &quot;Day Drinking&quot; set the tone for the album, motivating LBT to innovate with each new song. Recently selected as &quot;Song of the Week&quot; by </span><em><span lang="en" xml:lang="en">USA Today, </span></em><span lang="en" xml:lang="en">this first single continues to climb the charts.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">&quot;Tumble and Fall,&quot; written by the ladies of LBT and Lori McKenna, Liz Rose and Hillary Lindsey, is a promise to persevere in a relationship despite the challenges and offenses that naturally arise. &quot;It's a reminder to be humble. Be vulnerable. It's a peaceful song,&quot; Kimberly adds. Featuring Jimi's vocals and Kimberly's soaring harmonies, &quot;Tumble and Fall&quot; is both heartfelt and delightful.  LBT knew early on in song selection that &quot;Pain Killer&quot; would be the title track. &quot;Music, like medicine, can be a vice, a drug, a muse. But in this case, &quot;Pain Killer&quot; refers to the love drug,&quot; says Karen. It is the magic potion made real, solving all problems with one fell swoop and intoxicating in the best way. Written by Karen, Jimi, Blair Daly and Lindsey, it is an upbeat, reggae-tinged tune perfect for a road trip, best enjoyed while riding with one hand out the window or on the back of a lover's neck.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">Perhaps the most affecting, jaw-dropping track is the down-tempo &quot;Girl Crush.&quot; This attention-grabber is stripped down to a power vocal with sparse backing. Karen's soulful voice finds a fitting showcase against a retro beat, echoing the sounds of Patsy Cline and her contemporaries. Written by McKenna, Rose and Lindsey, it is one of the few songs to which every woman can relate. &quot;'Girl Crush' is one of the most brilliant lyrics I've ever heard. It takes a modern phrase and turns it at the hook. And it's empty in the right places. It gives me chills every time I listen to it because the raw emotion really comes through,&quot; Jimi explains.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">One of the more cinematic and barrier-breaking tracks is &quot;Faster Gun,&quot; written by Jeremy Spillman, Ryan Tyndell, Jimi and Phillip while in &quot;dude mode&quot; in the perfect place for men to be men – a man cave, conveniently located at the studio. &quot;Faster Gun&quot; is one of the best examples of new sounds and layers for LBT. It sounds like a Tarantino flick – raw and liberated. &quot;I could see it playing in my head like a trippy, acid western. It's completely different than anything we've done before,&quot; Phillip says. &quot;Faster Gun&quot; is the track that showcases LBT in a totally new light.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">&quot;Good People&quot; is a musical high five to partners in crime and is the glue binding all of the tracks together. &quot;We fell in love with it the minute we heard it. It felt great and we needed a moment like this on the record. It brought it to life,&quot; Phillip says. Joyce, Hemby and Spillman wrote the song which spotlights Kimberly's pure-tone soprano. This track is a gift to any friend who not only knows where the secrets are buried, but helped bury them.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">&quot;Stay All Night&quot; is upbeat, totally rockin' and full of life. &quot;I love the groove. The phrasing is rapid fire and very rhythmic. It's funky cool!&quot; explains Jimi. &quot;I'm so excited it made the record. The girls have lungs for days!&quot; Written by Jimi, Phillip, Brent Cobb and Jason Saenz, &quot;Stay All Night&quot; is the party song fitting for a no-holds-barred night out. Jimi's vocals cranked the dial to 11 while Joyce tuned guitar strings to one chord and used the entire instrument as a horn. Full of personality, &quot;Stay All Night&quot; is a shining example of sonic details masterfully woven.   Another powerful showcase of Kimberly's full and lively vocals is &quot;Save Your Sin.&quot; It was written by McKenna, Rose and Lindsey as a swift kick in the behind to someone less than worthy of another's heart. The upbeat and pulsing track is just what </span><em><span lang="en" xml:lang="en">Pain Killer </span></em><span lang="en" xml:lang="en">needs. &quot;Kimberly freaking killed it,&quot; Jimi says. &quot;It's like the Foo Fighters meets country with a big screaming vocal.&quot;
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">Written on the road in a dressing room by the whole band with Spillman and Tyndell, &quot;Live Forever&quot; features the traditional harmonies that first attracted fans and critics to LBT. &quot;It is the epic love song,&quot; says Phillip. &quot;Live Forever&quot; is a master class in harmonies. It is the beautiful and profound track that anchors the album and expands on the talent the world has come to expect from Little Big Town.    In contrast to the classic LBT song that is laced with romance and sweeping vocal harmonies, &quot;Things You Don't Think About&quot; is &quot;total sassville,&quot; says Kimberly. Written by Hemby, McAnally and Ross Copperman, it begins with a sparse groove followed by a chilling down beat. &quot;You feel this arresting, visceral energy the moment it comes on. It's a killer song about not taking someone for granted,&quot; Phillip adds.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">Little Big Town deeply understands and respects the creative process. They know the challenges a creative spirit faces in an unforgiving music industry. With this is in mind, they set out with Spillman, Hemby and Joyce to write a wake up call, &quot;Turn the Lights On.&quot; This hard-driving, rock n' roll hymn is especially for those brilliant minds that have to continually hear &quot;no&quot; before they ever hear the &quot;yes&quot; that changes everything. It's an inspiring and over-the-top reminder to anyone to get up off the mat and keep going. &quot;Standing up for yourself as an artist is the hardest lesson to learn. Artists aren't always nurtured once they become part of the business machine. It's a lot harder for solo artists, but we have each other for the gut check,&quot; says Karen.
                      <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">The album's coda, &quot;Silver and Gold,&quot; is a poetic, quiet song starring the characteristic LBT harmonies that have never been lost or lessened by time or circumstance. Karen, Kimberly, Joyce and Jedd Hughes penned it under the stained glass in Joyce's church-turned-studio. Kimberly says, &quot;Jedd Hughes is a poet and inspiration.&quot; A simple, sonic masterpiece backed by a solo acoustic guitar, &quot;Silver and Gold&quot; is an encouraging reminder for a heavy heart that good still lives inside. &quot;The vocals just wash over you,&quot; says Phillip.
                      <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">When reflecting on the entirety of </span><em><span lang="en" xml:lang="en">Pain Killer, </span></em><span lang="en" xml:lang="en">Jimi sums it up well. &quot;Being in a studio, creating music and a moment that means something to people is magic. We love this record. The creative part of us is satisfied.&quot;  With a keen focus on different vocal and writing configurations, LBT again astounds its fans and critics alike with harmonies that are typically found among voices sharing the same DNA. Their strengthening relationships and maturity earned over 15 years together all come together in this masterful production.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="en" xml:lang="en">A remedy for everything that ails any listener, </span><em><span lang="en" xml:lang="en">Pain Killer</span></em><span lang="en" xml:lang="en"> is an antidote of anthems and inspiration to heal even the most tortured heart. It is one big love letter to Little Big Town's fans.
                          <!-- o ignored -->
                    </span></p>
