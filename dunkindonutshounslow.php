                <p align="center"><strong><em><span lang="EN-US" xml:lang="EN-US"> </span></em></strong></p>
                <p align="center"><strong><em><span lang="EN-US" xml:lang="EN-US">First Dunkin’ Donuts store in Ghataaura Group Ltd agreement opens in Hounslow, with a day of family activities </span></em></strong></p>
                <p><strong><span lang="EN-US" xml:lang="EN-US"> </span></strong></p>
                <p><strong><span lang="EN-US" xml:lang="EN-US">LONDON, England (July 29, 2014)</span></strong><span lang="EN-US" xml:lang="EN-US"> – </span><span lang="EN-US" xml:lang="EN-US"><a href="http://www.dunkindonuts.com/" target="_blank">Dunkin’ Donuts</a></span><span lang="EN-US" xml:lang="EN-US">, one of the world’s leading coffee and baked goods chains, today announced that it is opening a new store in Hounslow, West London, in agreement with Ghataaura Group Ltd. </span></p>
                <p><span lang="EN-US" xml:lang="EN-US">To celebrate the opening, a family fun day will take place at the store on July 29th. From 7am customers will have the chance to win </span><span lang="EN-US" xml:lang="EN-US">free boxes of donuts, and the family fun activities - including</span><span lang="EN-US" xml:lang="EN-US"> f</span><span lang="EN-US" xml:lang="EN-US">ace painting, dancers and an NYPD police car with two policeman - will begin at 10am.</span></p>
                <p><span lang="EN-US" xml:lang="EN-US">This is the first restaurant to be opened as part of the Ghataaura Group Ltd agreement (the UK’s third franchise agreement for Dunkin’ Donuts) which calls for the development of 25 restaurants in the West London area. The brand plans to develop a total of 150 restaurants in the United Kingdom over the next five years and has already opened three restaurants over the last few months in Harrow, Chelmsford, Cambridge and Woolwich.</span></p>
                <p><span lang="EN-US" xml:lang="EN-US">“We’re pleased to announce the opening of our new Hounslow restaurant, in agreement with Ghataaura Group Ltd., whose leaders have a wealth of experience in the local area, to develop the Dunkin’ Donuts brand in West London,” said Jim Johnstone, General Manager, UK, for Dunkin’ Brands. “We look forward to keeping even more guests in the United Kingdom going with Dunkin’ Donuts’ delicious range of hot and iced beverages, sandwiches and baked goods.”</span></p>
                <p><span lang="EN-US" xml:lang="EN-US">Ghataaura Group Ltd. is a family business, owned and operated by Ash, Renu and Bob Ghataaura, who have already built successful businesses in both information technology and property. More recently, the group opened an ice cream and dessert parlour with Dunkin’ Donuts’ sister brand, Baskin-Robbins, on Chiswick High Road in London.</span></p>
                <p><span lang="EN-US" xml:lang="EN-US">“Dunkin’ Donuts is known around the world for its wide selection of high quality food and beverages, as well as its speed of service, so we’re proud to bring the Dunkin’ Donuts experience to Hounslow,” said Ash Ghataaura, Director of Ghataaura Group Ltd. “We look forward to becoming a destination of choice for our busy, on-the-go guests in the area.”</span></p>
                <p><span lang="EN-US" xml:lang="EN-US">Dunkin’ Donuts currently has nearly 11,000 restaurants in 33 countries around the world, including three locations in the UK.  Dunkin’ Donuts restaurants in the UK serve a wide range of hot and iced beverages, an assortment of sandwiches, baked goods, all-day snacks and grab and go products. The menu includes items specially tailored to the UK, including Sausage Butties, Bacon Butties, Porridge and Hot Croistrami™ Sandwiches. Dunkin’ Donuts also offers items from its DDSMART® lineup, including Latte Lite, Egg &amp; Cheese Wake Up Wraps, Yogurt and Multi-Seed Bagels.</span></p>
