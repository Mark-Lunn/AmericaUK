                <p><strong>Rainforest Cafe goes wild over interactive experience</strong></p>
                <p>London's largest family restaurant, the Rainforest Cafe, has introduced a new level of interactivity to its immersive jungle experience with the introduction of two floor-based Living Displays, giving young guests a stunning opportunity to play with moving fish, leaves and frogs.</p>
                <p>Described as 'a place for discovery', Rainforest Cafe offers an authentic tropical forest experience in the heart of London's Piccadilly Circus. With animatronic elephants, gorillas, snakes and jaguars, a thunderstorm every 29 minutes, Amazonian foliage, live fish and of course rainforest-themed food, General Manager Brendan Lucey says he was 'thrilled' to install the Living Floor systems offered by i-Sub Digital Solutions.</p>
                <p>Using an infrared tracking system, a video projector and a graphics PC running bespoke software, the Living Floor enables children and adults alike to fully interact with digital images, to play games and change the display with their hands and feet.</p>
                <p>Lucey chose two systems for Rainforest Cafe, one placed at the foot of the steps down into the restaurant area, giving diners a glimpse of the forest floor – complete with shimmering leaves and Rainforest Cafe's mascot, Cha! Cha! the red-eyed tree frog – as they arrive. On entering the projection area, the leaves blow around with the movement of feet, while frogs hop away. With guests surrounded by the flora, fauna and sounds of the rainforest, an authentic leafy floor completes the atmosphere. </p>
                <p>A second Living Floor was fitted deeper into the 'forest' to create a ground-level fishpond. The digital fish, designed as replicas of the live fish in tanks around the restaurant, swim happily until the projection is walked over when they dash away.</p>
                <p>The leaf floor and fish pool have been enormously popular with the thousands of guests hosted weekly by Rainforest Cafe since their installation ahead of the school holidays. </p>
                <p>Simon Yandell, Sales and Marketing Manager of Rainforest Cafe London, comments: &quot;People with young children have to tear them away from the Living Display to have dinner. It's such a mind-blowing experience for them.&quot;</p>
                <p>&quot;It's a perfect addition to the intention of Rainforest Cafe as a brand,&quot; Yandell adds. &quot;We offer a unique experience, and the Living Displays meet our priority of providing interactive engagement and immersion.&quot; </p>
                <p>The nature theme of the two Living Displays reflect Rainforest Cafe's charity partnerships, with tropical bird care organisation the Society for Conservation of Aviculture and the Aspinall Foundation. Working with the World Land Trust, Rainforest Cafe has also protected 3.2million sq m of Ecuadorian rainforest so far since 2001.</p>
                <p>Emma Plant, Director at i-Sub Digital Solutions, explains that her team worked closely with the Rainforest Cafe to ensure that the Living Floor systems would be complimentary to the experience already enjoyed by diners. </p>
                <p>&quot;High-end projectors and bespoke software applications were specified to meet the requirements at the Rainforest Cafe: a 3m ceiling height, fairly bright ambient light and a final image area of 4m x 3m dictated that a normal system would not suffice,&quot; states Plant. </p>
                <p>&quot;In addition, the final Living Floor was shaped to emulate a typical rainforest pond and stream, to make the effect as realistic as possible and fit in with the whole theme of the restaurant's interior design and functionality. There was also a prerequisite to mould it around the permanent layout of tables and chairs in the restaurant area and the waterfall structure in the bar area.&quot;</p>
                <p>&quot;It was a challenging project that required plenty of planning and attention to detail but the final result is fantastic,&quot; she concludes.</p>
