                <p><strong>Bodeans BBQ - American Diners in Soho, Clapham, Fulham and Tower Hill</strong></p>
                <p>Bodean’s BBQ, London’s leading transatlantic diner-deli, has been delivering ‘down home specials’ (including slow-smoked BBQ ribs), all American beers and live sports from across the pond, for over 10 years. </p>
                <p>The inaugural Bodean’s BBQ site was launched in Soho, London in 2002 by Andre Blais who has since opened three more restaurants in Clapham, Fulham and Tower Hill. Bodean’s BBQ is one of the most established slow-cooked meat restaurant groups in London and has influenced a host of imitators in recent years. <br />
                    <br />
                    <strong>BODEAN’S BBQ - FOOD &amp; DRINK </strong><br />
                  Bodean’s meat is carefully chosen for quality and provenance with just the right amount of marbling to keep it moist during the slow cooking process. It is dry rubbed with a complex blend of spices then smoked over oak in a “pit” for up to 12 hours just as they do in Kansas City, the vibrant centre of the USA’s great barbecue tradition.</p>
                <p><strong>Typical Dishes</strong><br />
                  The menu at Bodean’s BBQ offers quintessential North American favourites from tender slow smoked briskets to pulled pork and sweet BBQ ribs, with an emphasis on delivering a true American BBQ experience. This includes Tri Tips: a cut of prime beef from the lower cut of sirloin, rubbed in a special “cow crust”, slow smoked and served with a Samuel Adams and Mesquite BBQ sauce; Pulled Pork: pork shoulder marinated overnight and smoked for 12 hours, shredded with North Carolina sauce and served with a tangy coleslaw (£8.95); BBQ Spare Ribs: sweet and tender St Louis-cut ribs finished on the grill and served with Hickory sauce (£13.50); and diners with a bigger appetite will enjoy the Bodean’s Platter: includes a selection of baby back ribs, burnt ends, pulled pork and chicken thighs served with fries and coleslaw (£25.95).</p>
                <p><strong>Beer</strong><br />
                  Bodean’s BBQ offers a range of craft beers including Samuel Adams Boston Lager, brewed to a recipe from the 1870s (£3.95); Sierra Nevada Pale Ale bringing a West Coast taste from Chico, California (£5.25); Sierra Nevada Summerfest Lager, brewed in the Czech Bohemian tradition (£5.25); Odells Cutthroat Porter from the Indian wars outpost of Fort Collins, Colorado, at an altitude of 5,000 feet (£5.55); Blue Moon, inspired by Belgian witbier and distinguished with oats, wheat and a hint of Valencia orange peel (£4.95), as well as an extensive wine list with red, white, rosé, sparkling and Champagne options. <br />
                   
                  <br />
                  Bodean’s BBQ also offers its marinated meats and sauces to customers to cook at home, available in store or online.</p>
                <p><strong>BODEAN’S BBQ – DESIGN</strong></p>
                <p>Each of the Bodean’s BBQ restaurants were designed by Quentin Reynolds, who created the Egyptian escalator in Harrods and also worked on the homes of Madonna and Bernie Ecclestone.  The Bodean’s BBQ design is heavily influenced by American diners with dark leather booths teamed with neon signs, long horn skull light fittings and unique rock n roll art. Large wall-mounted televisions screen the latest American sports beamed live from the US.</p>
                <p><strong>BODEANS BBQ – RESTAURANTS</strong></p>
                <p>Bodean’s BBQ Soho opened in December 2002 on Poland Street in the heart of Soho minutes from Oxford Street Tube Station. It seats 120 people over a ground floor and basement eating areas.<br />
                   
                  <br />
                  Bodean’s BBQ Clapham opened in May 2004, 50 yards from Clapham Common and the Underground station. It seats 85 people over ground floor and basement eating areas as well as having a cocktail bar.<br />
                   
                  <br />
                  Bodean’s BBQ Fulham opened in March 2006 and is situated opposite Broadway Underground station and 100 yards away from Chelsea’s Stamford Bridge stadium. It seats 68 people and offers both take away and dine in options.<br />
                   
                  <br />
                  Bodean’s BBQ Tower Hill opened in July 2008 and is situated on Byward Street in the centre of the city. This location also houses a bar on the ground floor with over 30 American speciality hand craft beers. </p>
                <p><strong>Andre Blais </strong><br />
                   
                  <br />
                  Andre Blais, founder of Bodean’s BBQ, has extensive experience in the restaurant sector which spreads across Belgo, Signature PLC and Caprice Holdings. Until Bodean’s BBQ opened in 2002 few in the UK were aware of “Pulled Pork” or “Burnt Ends” and the only BBQ Ribs available had never seen a smoke pit. Blais was determined to change this. Armed with two friends, he headed to various establishments in the US with the primary objective of eating his way to source the knowledge and equipment to start London’s first authentic BBQ restaurant.<br />
                </p>
