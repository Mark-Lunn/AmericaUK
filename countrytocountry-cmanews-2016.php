                      COUNTRY TO COUNTRY  </strong></strong></p>
                    <p><strong></strong></p>
                    <p><strong><strong></strong></strong></p>
                    <p><strong><strong>AT THE O2, LONDON, UK / THE CLYDE AUDITORIUM, GLASGOW, UK / AT 3ARENA, DUBLIN, IRELAND</strong></strong></p>
                    <p><strong></strong></p>
