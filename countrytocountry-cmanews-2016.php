                    <p>Also at The O2 in London, the CMA Songwriters Series will take place on Thursday March 10th, there will be nightly after-show parties featuring special guests, plus over 30 up and coming artists will perform on pop-up stages across the site.  With over 40 performances a day on 5 free-to-watch pop up stages plus the Town Square showcasing whole host of food, drink and entertainment stands -  C2C truly brings the 'spirit of Nashville' over to these shores.  </p>
                    <p><br />
                    </p>
                    <p>The massively popular satellite stage will also be returning to The O2, London in 2016. Ashley Monroe is the first artist confirmed for the stage, with further acts to be revealed for the other two days in due course. Ashley will be performing on the mainstage in Glasgow and Dublin. Watch this space for more news to come. </p>
                    <p><br />
                    </p>
                    <p>&nbsp;</p>
                    <p>Artist information</p>
                    <p><br />
                    </p>
                    <p><strong>Miranda Lambert</strong></p>
                    <p>Miranda is the six-time reigning CMA and ACM Female Vocalist of the Year, a GRAMMY® winner with three celebrated Albums of the Year under her belt and she's already nominated in four categories for the 2015 CMA Awards, including the Entertainer and Female Vocalist categories.  A fantastic singer, she's also a successful songwriter and penned eight of the tracks on her latest best-selling US No. 1 album <strong><em>Platinum</em></strong> (RCA). She makes her UK and Irish debut with headlining performances at C2C 2016.</p>
                    <p><br />
                    </p>
                    <p><strong>Carrie Underwood</strong></p>
                    <p>She emerged from the promise of her 2005 American Idol win to become a true multi-format, multi-media superstar, spanning achievements in music, television, and film.  A seven-time GRAMMY® Award winner, she has amassed nearly 100 major honours, sold more than 56 million records worldwide, and recorded 21 #1 singles, 11 of which she co-wrote. Her latest album is <strong><em>Storyteller</em></strong> (Columbia) and includes the hit single &quot;Smoke Break&quot;.  Carrie returns to the UK after headlining the first C2C in 2013 and most recently the Apple Music Festival at London Roundhouse.  </p>
                    <p><br />
                    </p>
                    <p><strong>Eric Church</strong></p>
                    <p>One of American music's most electrifying entertainers and prolific songwriters, Eric Church has cultivated a devoted fan base without sacrificing musical integrity and self-expression, He's built up his following slowly, but the hard work paid off and he has five prestigious nominations for 2015 CMA Awards following the success of his recent fourth studio album, <strong><em>The Outsiders</em> </strong>(EMI Records Nashville). The release is the follow-up to the award-winning, <strong><em>Chief</em>,</strong> which included the single &quot;Springsteen&quot; that was particularly popular in the UK, A listed at BBC Radio 2 and which he performed on Later With Jools Holland. </p>
                    <p><br />
                    </p>
                    <p><strong>Dwight Yoakam</strong></p>
                    <p>On Dwight Yoakam's latest album, <strong><em>Second Hand Heart</em></strong> (Warner Bros./Reprise), the distinctive, supple vocals, accented with his Kentucky croon, sound as strong today as they did on his debut, 1985's <strong><em>Guitars, Cadillacs, Etc. Etc</em></strong>. That release by the multi-talented singer, songwriter and guitarist immediately yielded hit singles and over the course of some 21 albums – totalling more than 25 million in sales worldwide – Yoakam has continued to passionately sing, write, and play music brimming with hard country and rock &amp; roll. Yoakam is a 21-time nominated, multiple GRAMMY® Award winner. </p>
                    <p><br />
                    </p>
                    <p><strong>Little Big Town</strong></p>
                    <p>GRAMMY® Award-winners Little Big Town have had one of the biggest country hits of the summer with &quot;Girl Crush&quot; - a track from their sixth and latest album <strong><em>Pain Killer</em></strong> (Capitol) - which spent 13 weeks at No. 1 in the US. The four piece group comprises Karen Fairchild, Phillip Sweet, Kimberly Schlapman and Jimi Westbrook and they're the current CMA Vocal Group of the Year and are nominated for five CMA Awards this year.  They made their UK debut at The O2, London in 2013 as part of the very first C2C where they received a fantastic reception.</p>
                    <p><br />
                    </p>
                    <p><strong>Kacey Musgraves</strong></p>
                    <p>The super-talented singer-songwriter from Golden, Texas earned massive critical acclaim with the release of her stunning album <strong><em>Same Trailer Different Park</em></strong> (Mercury Records Nashville) including two GRAMMY® Awards, CMA and ACM honours.  Her latest album, <strong><em>Pageant Material</em></strong> has won similar praise and popularity and charted in the UK Top 20.  Kacey is hugely popular with listeners at BBC Radio 2 having had no less than seven singles playlisted and appeared at Radio 2 Live in Hyde Park in 2014.  She too has appeared on Later with Jools Holland</p>
                    <p><br />
                    </p>
                    <p><strong>Thomas Rhett</strong></p>
                    <p>It's only taken a couple of years for Thomas Rhett to become one of country music's hottest new artists.  The son of Rhett Akins who enjoyed several country hits in the 90s, Thomas didn't plan on being a singer and made his initial breakthrough as a hit songwriter for the likes of Luke Bryan and Blake Shelton  His debut album was <strong><em>It Goes Like This</em></strong>,  which spawned five Top 40 hits and three Numbers Ones. Now comes <strong><em>Tangled Up</em></strong> (Valory), his second album which mixes the sounds of his influences growing up listening to FM radio in the USA with equal doses of groove, melody and twang.  He makes his UK and Ireland debut at C2C 2016.</p>
                    <p><br />
                    </p>
                    <p><strong>Sam Hunt</strong></p>
                    <p>At first glance Sam Hunt might seem like a typical young country singer.  He grew up in a small Southern town, spent school days concentrating on sports but also feeling an attachment to music growing deeper. Then he came to Nashville with little idea of how the music business worked but with big dreams.  Look closer and you'll see there is nothing typical about the music that Hunt makes, nor the way he has introduced his work to the world. In a short time and on his own terms he has become one of Nashville's most hotly anticipated new artists and his debut album, <strong><em>Montevallo</em></strong> (MCA Nashville), delivers on the buzz and the promise—and then some.</p>
                    <p><br />
                    </p>
                    <p><strong>Chris Stapleton</strong></p>
                    <p>The Kentucky-born singer-songwriter and producer Chris Stapleton is one of Nashville's most revered craftsmen, with a 15-year career that includes writing No. 1 hits, GRAMMY® Award nominations, feature film contributions, the respect of his peers and now his first solo album, <strong><em>Traveller</em></strong> (Mercury Records Nashville) which debuted at #2 on the US Country Albums chart making it the best-selling first week by a debut country artist in 2015.  His songs have been covered by country and pop superstars, including Adele. He will make his UK and Ireland debut at C2C 2016.</p>
                    <p><br />
                    </p>
                    <p><strong>Ashley Monroe </strong></p>
                    <p>Most folks, as soon as they hear Ashley Monroe, instantly recognize one thing: she's the real deal. Her songs resonate so profoundly because they reflect the experiences of a young artist who has already seen both the best and worst that life has to offer. The Tennessee born singer-songwriter's new album <strong><em>The Blade</em></strong> was released in the US in July on Warner Bros. Nashville immediately going to No. 1 on iTunes Country Chart. The record was produced by friend and mentor Vince Gill and Justin Niebank.</p>
                    <p><br />
                    </p>
                    <p><strong>Maddie &amp; Tae</strong></p>
                    <p>Maddie Marlow and Tae Dye didn't set out to be the revolutionaries of the Country mainstream. They just wanted to write songs about life the way two 18-year old young women (now 20 and 19) were living it. Songs on their debut album, <strong><em>Start Here</em></strong> (Decca) include themes about bullying (&quot;Sierra&quot;), clueless boys (&quot;Shut Up &amp; Fish&quot;), the power of friendship (&quot;After the Storm Blows Through&quot;) and coming of age (&quot;The Down Side of Growing Up&quot;).  But when they released &quot;Girl In A Country Song&quot; about the reductive tilt of today's &quot;Bro Country&quot; they had a sensational response which was instant and intense and made them stars.  They make their UK debut at C2C 2016.<strong> </strong></p>
                    <p><br />
                    </p>
                    <p><strong>Andrew Combs </strong></p>
                    <p>A Dallas, Texas native but now Nashville-based, Andrew Combs' second album, <strong><em>All These Dreams</em></strong>, (Loose Music) marks a huge step forward for the singer-songwriter who American Songwriter magazine said, &quot;embodies the best qualities of Nashville's ever expanding talent pool&quot;. He returns to the UK for C2C having most recently been here for the End of the Road Festival. Using his gifts for lyricism and wry observation, Combs weaves tales of love, sin and redemption on the new album, finally reaching the heights of his Texas songwriting heroes. </p>
                    <p><br />
                    </p>
                    <p><strong>QUOTES:</strong></p>
                    <p><br />
                    </p>
                    <p>The legendary BBC Radio 2 broadcaster Bob Harris, who will again be compering C2C London says, <strong><em>&quot;I am proud and honoured to be the main stage compere at C2C again this year, and delighted to be at the heart of Radio 2's coverage of this magnificent event which has done so much to raise the profile of country music in the UK. I'm sure that we're all going to have a wonderful time – I can't wait!&quot;</em></strong></p>
                    <p><br />
                    </p>
                    <p>Chris York, C2C Festival promoter for SJM Concerts said <strong><em>&quot;we are delighted to bring Country to Country back to the UK and Ireland bigger and better. More artistes, more cities and more nights to see the best in Country music. Look forward to seeing all of the amazing fans again in 2016. Great to be working with our partners BBC Radio 2 and the Country Music Association to deliver this event once more.&quot;</em></strong></p>
                    <p><br />
                    </p>
                    <p>Sarah Trahern from the CMA said: <strong><em>&quot;C2C continues to be a valued partner in the growth of Country Music in the UK and Europe.  The expansion of C2C speaks to the popularity of the music, the artists and the enthusiastic response from our international fans. The inclusion of the CMA Songwriters Series for the third year in a row further demonstrates the UK fans' appreciation for the Songwriters and their craft which is the foundation of Country Music.&quot;</em></strong></p>
                    <p><br />
                    </p>
                    <p>Little Big Town who were at the London launch, said<strong><em> &quot;We are so excited to be coming back to C2C and performing for our fans in the UK and Ireland.  We had the most incredible time playing in 2013 and are honoured to return with such an amazing line up.&quot; </em></strong></p>
