                    </p>
                    <p><strong>COUNTRY TO COUNTRY 2016 LINE UP WIN BIG AT THE ANNUAL CMA AWARDS, IN NASHVILLE, USA</strong></p>
                    <p><strong></strong><br />
                    </p>
                    <p>Country To Country, the UK and Ireland's largest country music festival is back in March 2016 featuring America's biggest stars including <strong>Miranda Lambert, Carrie Underwood, Eric Church, Dwight Yoakam, Little Big Town, Kacey Musgraves, Thomas Rhett, Sam Hunt </strong>and<strong> Chris Stapleton </strong>plus<strong> Maddie and Tae, Andrew Combs</strong> and <strong>Ashley Monroe</strong> and some of them swept the board at the annual CMA Awards in Nashville. </p>
                    <p><br />
                    </p>
                    <p><strong>Chris Stapleton</strong> and <strong>Little Big Town</strong> were the big winners at the CMA Awards last night.  Chris Stapleton won Best Male, Best Newcomer and Album of the Year.  His performance of Tennessee Whiskey with Justin Timberlake completely stole the show.   </p>
                    <p><br />
                    </p>
                    <p>Along with a live performance, <strong>Little Big Town</strong> won Best Group and their Radio 2 playlisted track Girl Crush was awarded best song and single.  <strong>Miranda Lambert</strong> won Female Vocalist of the Year for the sixth time and <strong> Maddie &amp; Tae</strong> won Best Video.</p>
                    <p><br />
                    </p>
                    <p><strong>Eric Church </strong>surprised everyone by releasing his previously unannounced new album Mr Misunderstood on CMA Awards day in which he performed the title track.<strong> </strong> If that wasn't enough other standout performances came from show host <strong>Carrie Underwood</strong> who performed her Radio 2 playtlisted track Smoke Break and fellow playlister <strong>Kacey Musgraves</strong> who performed Dime Store Cowgirl plus <strong>Thomas Rhett</strong> with his song Crash &amp; Burn.  </p>
                    <p><br />
                    </p>
                    <p>For the first time Country To Country will be held over three consecutive days – Friday 11, Saturday 12 and Sunday 13 March, 2016 – in three cities; The O2, London, Glasgow Clyde Auditorium and Dublin 3Arena.  </p>
                    <p><br />
                    </p>
                    <p>It follows the storming success of C2C 2015 earlier this year when 80,000 country fans packed out The O2, London and 3Arena Dublin for C2C 2015 that saw multimillion selling megastars Luke Bryan, Lady Antebellum, Florida Georgia Line, Jason Aldean, and acclaimed artists Lee Ann Womack, Brantley Gilbert, Brandy Clark and Kip Moore take to the main stage as well as launching the brand new satellite stage which saw performances from UK talent such as The Shires, Ward Thomas and Sam Palladio.</p>
                    <p><br />
                    </p>
                    <p>Country To Country is an incredible opportunity for fans to see their idols come together over one weekend, under one roof and is not to be missed. Venues/dates and line-up are as follows:</p>
                    <p><br />
                    </p>
                    <p>Friday 11th to Sunday 13th March 2016 </p>
                    <p><strong>The O2, London, UK</strong></p>
                    <p><strong>The Clyde Auditorium, Glasgow, UK </strong></p>
                    <p><strong>3Arena, Dublin, Ireland</strong></p>
                    <p><br />
                    </p>
