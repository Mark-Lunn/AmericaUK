                <p>An <a href="http://www.ford.co.uk/ConfigureyourCar" target="_blank">online configurator</a> will enable customers to specify their Mustang and review pricing.  In addition, smartphone and tablet users can see and share exactly how their chosen Mustang could look, using a new augmented reality app that places the car in the setting of their choice.</p>
                <p>Available for Apple and Android devices, the app enables customers to first select bodystyle, exterior colour, and alloy wheel options. They can then view the car in their chosen setting in real-time using the camera feature of their mobile device. Users can even hear realistic engine sounds, and also share photos of their virtual Mustang. </p>
                <p>“There’s never been a better time to introduce this iconic car across Europe. All-new Ford Mustang is the most modern, technologically advanced and forward-looking Mustang in the amazing history of the marque,” said Roelant de Waard, vice president, Marketing, Sales &amp; Service, Ford of Europe. “Being able to specify a Mustang that reflects your individual character has always been a key part of the model’s appeal. With our new app, customers will be able to see exactly how the all-new Mustang could look outside their house, and as a part of their life.”</p>
                <p>Available with six-speed manual or automatic transmission, all-new Ford Mustang features as standard: 19-inch wheels, a performance brake package, automatic HID xenon headlamps, LED tail lamps, and a rear diffuser. Further standard equipment includes dual-zone climate control, a nine-speaker sound system, and SYNC 2 voice control connectivity with an eight-inch colour touch screen.</p>
                <p>Additional options include climate control seats and premium 19-inch alloy wheels with Lustre Nickel finish (available as part of the optional custom pack). Ten exterior colour choices include exclusive Competition Orange and tri-coat Triple Yellow.</p>
                <p>The all-new Ford Mustang is exclusively available to order in the UK at new FordStores. Located mostly in metropolitan areas, Ford is currently rolling out the stores across the country. </p>
                <p>Never before has Mustang gone on sale worldwide and the all-new Mustang will be available to customers in more than 100 markets. First shipments are headed for Asia, with vehicles arriving in the UK in late autumn..</p>
