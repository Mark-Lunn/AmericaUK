                <p>About Dunkin’ Donuts</p>
                <p><br />
                  Founded in 1950, Dunkin' Donuts is America's favorite all-day, everyday stop for coffee and baked goods. Dunkin' Donuts is a market leader in the hot regular/decaf/flavored coffee, iced coffee, donut, bagel and muffin categories. Dunkin' Donuts has earned the No. 1 ranking for customer loyalty in the coffee category by Brand Keys for seven years running. The company has nearly 10,800 restaurants in 32 countries worldwide. For the full-year 2012, Dunkin' Donuts' restaurants had global franchisee-reported sales of approximately $6.9 billion. Based in Canton, Mass., Dunkin' Donuts is part of the Dunkin' Brands Group, Inc. (Nasdaq: DNKN) family of companies. For more information, visit www.DunkinDonuts.com</p>
