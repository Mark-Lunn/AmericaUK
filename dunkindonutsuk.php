                <p>EXCITEMENT BREWS WITH GRAND OPENING OF FIRST UK DUNKIN’ DONUTS RESTAURANT IN HARROW</p>
                <p>Additional Restaurants Planned to Open Soon in Chelmsford and Cambridge</p>
                <p>LONDON, UK (December 17, 2013) – Dunkin’ Donuts, one of the world’s leading baked goods and coffee chains, today announced the grand opening of its first UK restaurant located at 51 St. Anns Road in Harrow. To celebrate, Dunkin’ Donuts and franchisees Ram Javia and Neil Patel are hosting a community-wide celebration at the restaurant with fun activities for guests throughout the day including in-store promotions, giveaways, live entertainment and a ribbon cutting ceremony with Dunkin’ Donuts executives and local dignitaries. Two additional Dunkin’ Donuts restaurants will opening in Chelmsford and Cambridge early 2014.</p>
                <p> “We’re excited to celebrate the grand opening of our first Dunkin’ Donuts location in the UK,” said Jim Johnstone, General Manager, UK, for Dunkin’ Brands. “Our new restaurant in Harrow will offer a delicious range of high-quality beverages, sandwiches and baked goods, served all-day and at a great value. We look forward to opening more restaurants in the months ahead across North and East London with our incredible franchisees, including  two upcoming locations in Chelmsford and Cambridge.”</p>
                <p><br />
                  The new Dunkin’ Donuts restaurant in Harrow will serve a wide range of hot and iced beverages, including Dunkin’ Donuts’ famous Original Blend Coffee and hot tea, in addition to an assortment of sandwiches, baked goods, all-day snacks and grab and go products. The menu includes items specially tailored to the UK, including Sausage Butties, Bacon Butties, Porridge and Hot Croistrami Sandwiches. Dunkin’ Donuts also offers items from its DDSMART® lineup, including Latte Lite, Egg &amp; Cheese Wake Up Wraps, Yogurt and Multi-Seed Bagels.</p>
                <p><br />
                  “We can’t wait to serve our guests in Harrow Dunkin’ Donuts’ famous lineup of beverages and snacks to keep them going throughout the day,” said Neil Patel, Dunkin’ Donuts franchisee. “We look forward to becoming an integral part of the local community and our guests’ daily routines, and to opening more restaurants in Greater London in the coming months.”</p>
                <p>The restaurant features Dunkin’ Donuts’ latest store design, which includes bright and modern décor, digital menu displays and comfortable lounge-style seating, creating a warm and inviting environment for guests to enjoy the brand’s range of food and beverages. The restaurant also offers free Wi-Fi Internet access to its guests</p>
                <p><br />
                  As Dunkin’ Donuts begins opening restaurants in the UK, it continues to look for qualified, multi-unit franchisee candidates who have a strong financial background, a deep knowledge of the local market, a proven track record of success in the restaurant industry and a desire to develop a minimum of 25 Dunkin’ Donuts restaurants in their area over the coming years. For more information about Dunkin’ Donuts franchising opportunities in the UK, please email james.johnstone@dunkinbrands.com</p>
