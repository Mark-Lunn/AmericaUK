                    <p><br />
                      With their hearts in the Home Counties and their heads in Nashville, USA, The Shires combine the best of both musical worlds on their eagerly anticipated debut album ‘Brave’. </p>
                    <p><br />
                      Combining the classic pop-rock drive of Fleetwood Mac with the uplifting singalong anthems of The Lumineers, British duo Crissie Rhodes and Ben Earle are being tipped for great things in 2015 by industry insiders - as well as their growing legion of fans.</p>
                    <p><br />
                      The Shires have also become the first UK signing to the revived Decca Nashville label. Originally launched 80 years ago, Decca Nashville became home to a host of country superstars including Patsy Cline and Loretta Lynn, who was then known as the “Decca Doll”. From the 1950s, Decca Nashville was one of the leading labels in America for country music, and remained so for a couple of decades. Decca was also the label for Country Queen Dolly Parton between 1997 and ‘98.</p>
                    <p><br />
                      The Shires formed after Ben (from Hertfordshire) appealed on Facebook for a female country singer to work with him. Crissie (from Bedfordshire), a friend of a friend, replied, came round the very next day and they discovered an instant chemistry.<br />
                      “We can build our own Nashville underneath these grey skies,” they sing on ‘Nashville Grey Skies’, the infectious radio hit that introduced them last summer and hoisted a flag for home-grown country music. </p>
                    <p><br />
                      They may have been newcomers to each other – but not to music. ‘Brave’ shows the evidence of Ben’s ten years paying his dues as a solo singer-songwriter and Crissie’s experience on the live circuit. And together, their distinctive voices blend perfectly.<br />
                      Both are inspired by the old-school workmanship of country music’s professional songwriters – people they would later work with on their first trip to Nashville last summer. Learning at first hand the storytelling style of classic country songs, and how to craft a catchy chorus, the duo then injected elements that could only have come from our side of the pond. </p>
                    <p><br />
                      The contagious new single ‘Friday Night’ (also released 23rd February) is sure to become an end-of-the-week anthem for all those ‘9 to 5’ers, and is perhaps the natural sequel to Dolly Parton’s classic, uniting all office workers.</p>
                    <p><br />
                      Powered by Crissie’s powerful but subtle and supple vocals and Ben’s perfectly complementary tones and acoustic guitar, The Shires have literally found a piece of country to call their own. They love American music, but they’re proudly British and intensely keen to add some real indigenous flavour to the pot. The song ‘Made In England’ is a perfect example, a real celebration of all things British. It is surely the first country song ever to celebrate rainy days, milky tea and the uniquely British pleasure of eating fish’n’chips in your coat on a cold pebble beach. “Look at those grey skies,” they sing. “They’re so beautiful.” </p>
                    <p><br />
                      The Shires tour the UK this year, supporting American country stars Little Big Town, before launching their own headline tour in April and, if their own lyrics are anything to go by: “People will come, they’ll come from far and wide. They’ll leave their village greens and the big cities behind, to build our own Nashville – it’s about time.” It certainly is.</p>
                    <p><br />
                      Announcing the details of their thirteen-date headline tour of the UK’s major cities in April 2015. The band will kick things off in Leeds on 8th April and run through until 26th April with their recently added show at Norwich Waterfront. Tickets are on sale now and available at www.gigsandtours.com </p>
