                <p><strong>28 June, London: </strong>Taco Bell, the world's largest Mexican-style restaurant chain, is opening its tenth UK outlet at Meridian Point in Cleethorpes on Tuesday, July 19, 2016. To celebrate the occasion, the first 100 customers on opening day will score an exclusive 'Cleethorpes First 100' T-Shirt for free with their order!
                    <!-- o ignored -->
                </p>
                <p>As the global leader in Mexican food, Taco Bell strives to bring Mexican-inspired cuisine to its fans around the world, and to create an experience and relationship that goes beyond food. The Cleethorpes restaurant features the innovative design of Taco Bell International. Exuding a youthful, urban look with influences from Mexico and the brand's California based headquarters, the restaurant has an open kitchen which showcases quality ingredients and lets consumers watch their food being made fresh to order. The restaurant has 50 seats, free Wi-Fi and charging points for customer's phones so they can connect and recharge while they eat.
                    <!-- o ignored -->
                </p>
                <p>The Cleethorpes restaurant also has the added convenience of a Drive Thru window, being the first Drive Thru Taco Bell to open in the UK.
                    <!-- o ignored -->
                </p>
                <p>Customers can look forward to the craveable, made to order classics like Crunchy Tacos, Crunchwrap Supremes, Chicken Quesadillas and UK specific menu items like the Fajita Burrito and Mexican Fries. For those customers who think a beer is the perfect pairing to a taco, Sol Cerveza will also be available in the Cleethorpes restaurant.
                  <!-- o ignored -->
                </p>
                <p>Speaking about the launch <strong>Mr. Nick Dawson, General Manager – Taco Bell Europe </strong>said <em>&quot;This is the sixth Taco Bell restaurant that our franchisee Northgate Fast Food have opened in the UK. We are proud to have such a great local partner, with a strong synergy to the brand and a vision to continue to bring Taco Bell to the people of the UK.&quot;</em>
                    <!-- o ignored -->
                </p>
                <p><strong>Mr. Arjun Patel of Northgate Fast Food </strong>said <em>&quot;We have had such a great response from each of our Taco Bell restaurant openings and are really excited to be able to bring that enthusiasm to Cleethorpes. Along with being able to provide our signature Mexican-inspired eats to both holiday makers and locals alike, we have created an additional 30 jobs for the locals in the area. Being a part of the community is such an important thing for us and the Brand, so we look forward to Bringing Más to Cleethorpes!&quot;.</em>
                    <!-- o ignored -->
                </p>
                <p> 
                    <!-- o ignored -->
                </p>
                <p>Taco Bell Cleethorpes will be the first Taco Bell opening of 2016 and will then be followed by a handful of others before the end of the year.
