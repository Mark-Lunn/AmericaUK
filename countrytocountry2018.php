                    <p><strong><span lang="EN-US" xml:lang="EN-US">FAITH HILL &amp;TIM MCGRAW 
                      <!-- o ignored -->
                    </span></strong></p>
                    <p><span lang="EN-US" xml:lang="EN-US">Faith Hill and Tim McGraw have been dominating the music charts since the early '90s. They began touring together in 1996 on McGraw's Spontaneous Combustion tour.  In 2000 they created the Soul2Soul franchise with their record-breaking &quot;Soul2Soul&quot; tour, becoming that year's top grossing and most successful U.S. tour. Their success continued in 2006-2007 when &quot;Soul2Soul II&quot; returned to the road, which was the most attended tour of the year in any musical genre and remains the highest grossing multi-year North American tour in country music history.
                          <!-- o ignored -->
                    </span></p>
                    <p><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                       
                    </span></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">KACEY MUSGRAVES
                      <!-- o ignored -->
                    </span></strong></p>
                    <p>The Texas native has earned massive critical acclaim for her first two albums, the Gold-certified debut Same Trailer Different Park in 2013 and Pageant Material in 2015. Musgraves took home the award for &quot;New Artist of the Year&quot; at 2013's CMA Awards and also won two Grammy Awards for Best Country Song (&quot;Merry Go 'Round&quot;) and Best Country Album (Same Trailer Different Park), the ACM Award for Album of the Year (Same Trailer Different Park), and most recently the 2014 CMA Award for Song of the Year (&quot;Follow Your Arrow&quot;). The Kacey Musgraves Country &amp; Western Rhinestone Revue has sold-out famous venues all over the world, including the Royal Albert Hall in London.
                        <!-- o ignored -->
                    </p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                       
                    </span></strong></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">LITTLE BIG TOWN
                            <!-- o ignored -->
                    </span></strong></p>
                    <p><span lang="EN-US" xml:lang="EN-US">Little Big Town-consisting of members Karen Fairchild, Phillip Sweet, Kimberly Schlapman, and Jimi Westbrook – first entered the music scene more than 18 years ago. This will be their first-time headline Country To Country.  Praised for their flawless &quot;silvery vocal harmonies&quot; (The Guardian) and their &quot;irresistibly upbeat&quot; (Rolling Stone) style, Little Big Town are an award-winning live act with prowess and effortless on-stage chemistry.  Little Big Town have earned almost 40 award show nominations and in the past five years has taken home nearly 20 awards, including multiple Grammy, CMA and ACM awards, and an Emmy award.
                          <!-- o ignored -->
                    </span></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                       
                    </span></strong></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                       
                    </span></strong></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">KELSEA BALLERINI
                      <!-- o ignored -->
                    </span></strong></p>
                    <p>Kelsea Ballerini skyrocketed to stardom with the US Top 5 release of her GOLD debut <em>THE FIRST TIME</em> producing three consecutive PLATINUM-certified, #1 smashes - &quot;Love Me Like You Mean It,&quot; &quot;Peter Pan,&quot; and &quot;Dibs&quot; – a feat never achieved by a female (including duos or groups) in Country music history. Her fourth single, &quot;Yeah Boy,&quot; also achieved GOLD status and rose to the lofty perch of #3 on Billboard's Country Airplay chart.
                        <!-- o ignored -->
                    </p>
                    <p><strong>
                      <!-- o ignored -->
                       
                    </strong></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">LUKE COMBS
                      <!-- o ignored -->
                    </span></strong></p>
                    <p><span lang="EN-US" xml:lang="EN-US">With gritty, grizzled vocals, brazen songwriting talent and one hell of a live show, River House Artists/Columbia Nashville's Luke Combs stormed onto country music landscape as a force to be reckoned with. Named a 2017 Artist To Watch by Amazon Music, Billboard, Bobby Bones, CMT, Huffington Post, Nashville Lifestyles, Pandora &amp; Rolling Stone, the 27 year-old Asheville, North Carolina native released his debut This One's for You on June 2 and opened with 43,000 total activity in sales &amp; consumption, making it the largest album debut from a new country artist in 2017.
                          <!-- o ignored -->
                    </span></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                       
                    </span></strong></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                       
                    </span></strong></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">LINDSAY ELL
                      <!-- o ignored -->
                    </span></strong></p>
                    <p><span lang="EN-US" xml:lang="EN-US">Named one of The Huffington Post's &quot;Top Country Artists to Watch,&quot; Lindsay Ell is a triple threat:  accomplished musician, unique vocalist and songwriter. The young Calgary native learned to play guitar while traveling with her father to country-bluegrass camps as a young girl. Ell honed her craft as a musical stylist and songwriter after being discovered by BTO and The Guess Who's Randy Bachman (&quot;American Woman&quot; / &quot;Taking Care of Business&quot;) at the age of 13.  Ell's highly anticipated debut EP, Worth The Wait (produced by Kristian Bush of Sugarland), debuted at No. 8 on the Nielsen Soundscan Country Albums Chart. For more information visit: </span><a href="http://www.lindsayell.com/" target="_blank" rel="noreferrer"><span lang="EN-US" xml:lang="EN-US">http://www.LindsayEll.com</span></a><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                    </span></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                       
                    </span></strong></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">EMMYLOU HARRIS
                      <!-- o ignored -->
                    </span></strong></p>
                    <p><span lang="en" xml:lang="en">With a career that has sold more than 15 million albums worldwide, garnered a remarkable 12 Grammy Awards and stirred music lovers for more than thirty years and counting, Emmylou Harris has been rightfully hailed as a major figure in several of America's most important musical movements of the past three decades. A steadfast supporter of roots music and a skilled interpreter of compelling songs, she also has been associated with a diverse and dazzling array of admiring collaborators from Bright Eyes to Tammy Wynette and from Neil Young to Waylon Jennings and Johnny Cash<strong>.
                            <!-- o ignored -->
                    </strong></span></p>
                    <p><strong><span lang="en" xml:lang="en">
                      <!-- o ignored -->
                       
                    </span></strong></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">JILLIAN JACQUELINE
                      <!-- o ignored -->
                    </span></strong></p>
                    <p><span lang="EN-US" xml:lang="EN-US">With words and music to live by, the twenty-something singer/songwriter is well sculpted. Real, true and humble, yet admirably confident, the traits resonate in her music. With pop melody sensibility, Jillian takes artistic liberty with her lyrics, but they're as flat-out and straightforward &quot;honest&quot; as they come. Her new recording project, Honestly, places Jillian's life under a magnifying glass to reveal unbound, themes of regret, nostalgia, heartache and frolic.  Featuring song titles like &quot;Hate Me,&quot; &quot;Reasons&quot;, &quot;God Bless This Mess&quot;&quot; the emotionally-weighted album spotlights &quot;less is more&quot; tracks that clearly demonstrate a comfortable intimacy to unveil Jillian Jacqueline's identity—undisguised.
                          <!-- o ignored -->
                    </span></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                       
                    </span></strong></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">ASHLEY MCBRYDE 
                      <!-- o ignored -->
                    </span></strong></p>
                    <p><span lang="EN-US" xml:lang="EN-US">Ashley McBryde, the fierce singer/songwriter who The Tennessean vows is &quot;worth discovering&quot; and Rolling Stone calls a &quot;Arkansas red-clay badass, with the swagger of Hank Jr. and the songwriting of Miranda Lambert,&quot; recently made her Grand Ole Opry debut with her heartrending ode-to-self, &quot;Girl Goin' Nowhere,&quot; which she released last month due to demand.
                          <!-- o ignored -->
                    </span></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                       
                    </span></strong></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">KIP MOORE
                      <!-- o ignored -->
                    </span></strong></p>
                    <p><span lang="EN-US" xml:lang="EN-US">Over the last few years, Kip Moore has spent most of his time on the road, building one of country music's most loyal audiences. He's a road warrior, armed with a live show that mixes the bombast and wild desperation of Bruce Springsteen with the rootsy stomp of Merle Haggard, living out of a tour bus with his bandmates and playing more than 200 shows per year. For a songwriter who'd grown up in a quiet pocket of southern Georgia, performing to crowds across the world — crowds that knew every word to his albums, </span><em><span lang="EN-US" xml:lang="EN-US">Up All Night</span></em><span lang="EN-US" xml:lang="EN-US"> and </span><em><span lang="EN-US" xml:lang="EN-US">Wild Ones</span></em><span lang="EN-US" xml:lang="EN-US">— felt like a dream come true.  Kip's latest release is a 5-track EP, album is called </span><strong><em><span lang="EN-US" xml:lang="EN-US">Underground</span></em></strong><span lang="EN-US" xml:lang="EN-US">.</span><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                    </span></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                       
                    </span></strong></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">LUKAS NELSON
                      <!-- o ignored -->
                    </span></strong></p>
                    <p><span lang="EN-US" xml:lang="EN-US">Raised in a musical family,</span><span lang="EN-US" xml:lang="EN-US"> </span><span lang="EN-US" xml:lang="EN-US">the son of Willie Nelson; Lukas Nelson learned early on that true originality is hard won. Since forming Promise of the Real 10 years ago, Lukas and his bandmates have played hundreds of shows including major festivals and built a devoted, ever expanding fan base. In 2014, destiny came knocking when Neil Young recruited Promise of the Real to be his road and studio band.  The new album &quot;Lukas Nelson &amp; Promise of the Real&quot; is out now and has received rave reviews. With a bounty of invaluable experience under their belt, expect the guys to deliver an indelible blast cosmic country soul, the sound of American music reborn.
                          <!-- o ignored -->
                    </span></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                       
                    </span></strong></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">MIDLAND
                            <!-- o ignored -->
                    </span></strong></p>
                    <p><span lang="EN-US" xml:lang="EN-US">Originally formed in Dripping Springs, Texas, Lead vocalist Mark Wystrach fronts Midland's rich sound, rounded out by lead guitarist Jess Carson and bass player Cameron Duddy. Rooted in tradition both in sound and style, the band initially garnered attention playing clubs in the Texas region. Their self-titled debut EP features five tracks, boasting songwriting credits from each member. Midland intentionally nods to the wild west with Country music that is reminiscent of the 1970s and 80s with a modern day twist. They've opened for idols Dwight Yoakam and Willie Nelson and are currently in the studio with Shane McAnally (Sam Hunt, Reba McEntire, Kacey Musgraves) working on their full length release for Big Machine Records.<strong>
                      <!-- o ignored -->
                    </strong></span></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">
                      <!-- o ignored -->
                       
                    </span></strong></p>
                    <p><strong><span lang="EN-US" xml:lang="EN-US">MARGO PRICE
                      <!-- o ignored -->
                    </span></strong></p>
                    <p><span lang="EN-US" xml:lang="EN-US">One of the finest new voices to emerge from the U.S.A., Margo Price was the first country signing to Third Man. Her critically acclaimed debut album chronicles her life to date and pulls no punches as she sings about very real hardships and heartaches – the loss of her family's farm, the death of her child, drinking and brushes with the law on this deeply moving collection of ten original tracks.</span><span lang="EN-US" xml:lang="EN-US"> </span><span lang="EN-US" xml:lang="EN-US">Her voice has that alluring mix of vulnerability and resilience that was once the province of Loretta and Dolly. It is a tour-de-force performance that is vivid, deeply moving and all true.
                          <!-- o ignored -->
                    </span></p>
