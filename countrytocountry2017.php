                    <p><strong>Artist information</strong></p>
                    <p>&nbsp;</p>
                    <p><strong>Brad Paisley</strong></p>
                    <p>A critically acclaimed singer, songwriter, guitarist and entertainer whose talents have earned him numerous awards, including three GRAMMYs, two American Music Awards, 14 Academy of Country Music Awards and 14 Country Music Association Awards (including Entertainer of the Year), among many others. He has been a proud member of the Grand Ole Opry since 2001. Paisley has written 20 of his 23 #1 hits including &quot;He Didn't Have to Be,&quot; &quot;Mud on the Tires,&quot; &quot;She's Everything,&quot; &quot;Water&quot; and &quot;Remind Me,&quot; among others.  </p>
                    <p><strong></strong><br />
                    </p>
                    <p><strong>Brothers Osborne </strong></p>
                    <p>EMI Records Nashville's Brothers Osborne comprises John and TJ Osborne. They released their highly anticipated debut album, Pawn Shop (co-produced by them with the award-winning Jay Joyce) early in 2016 and earned their first ever GRAMMY nomination for their Top 5 hit &quot;Stay A Little Longer&quot;. The RIAA Gold-certified tune continues to garner critical acclaim with Rolling Stone applauding the &quot;epic and inspired picking that identify the Maryland natives as one of Nashville's best.&quot; </p>
                    <p><br />
                    </p>
                    <p><strong>Cam</strong></p>
                    <p>California-born singer/songwriter Cam has emerged as one of the true breakthrough artists of the last year with first #1 country single with &quot;Burning House,&quot; which earned her a GRAMMY<em>® </em>Award.  Her <strong><em>Untamed</em></strong> album on Arista Nashville/RCA Records debuted at #2 on <em>Billboard</em>'s Top Country Albums chart with the year's best first-week album sales by a debut country artist. She kicked off 2016 on Brad Paisley's <em>Crushin' It Tour</em> and is about to end her six-month slot on Dierks Bentley's <em>Somewhere On A Beach Tour. </em></p>
                    <p><em></em><br />
                    </p>
                    <p><strong>Chris Young</strong></p>
                    <p>With five albums to his credit, Chris Young has amassed eight No. 1 singles, and fifteen Gold/Platinum certifications. Among the RCA Records artist's hits are &quot;Gettin' You Home,&quot; &quot;Voices,&quot; &quot;Tomorrow,&quot; &quot;I'm Comin' Over&quot; and &quot;Think Of You,&quot; the CMA Award nominated a duet with Cassadee Pope, Young's latest No. 1 and fastest-rising single of his career. Released in November of 2015 and debuting at No. 1 on Billboard's Top Country Albums chart, Young's first co-produced album, <em>I'm </em><strong><em>Comin' Over</em></strong>, also includes the new single, &quot;Sober Saturday Night&quot; and the three-week No. 1 smash title track.  </p>
                    <p><strong></strong><br />
                    </p>
                    <p><strong>Dan + Shay</strong></p>
                    <p>Their 2014 debut <strong><em>Where It All Began</em></strong><em>, </em>made them the first new country duo in SoundScan- history to debut at No. 1 on <em>Billboard</em>'s Country Albums Chart. The album included the debut single, &quot;19 You + Me&quot; and their first No. 1 &quot;Nothin' Like You,&quot; Dan + Shay's sophomore album <strong><em>Obsessed</em></strong>, is a 10-song project including the No. 1 single, &quot;From The Ground Up&quot;. Racking up more than 51 million YouTube views, the pair has impressively sold more than 2.2 million digital tracks and more than 620,000 album equivalents to date, including 240 million streams. </p>
                    <p><strong></strong><br />
                    </p>
                    <p><strong>Darius Rucker</strong></p>
                    <p>He first attained multi-platinum status as lead singer of GRAMMY award-winning Hootie &amp; the Blowfish. Since re-introducing himself as a country artist, he has released four consecutive albums to top the <em>Billboard </em>Country albums chart and earned a whole new legion of fans. Rucker's first two country albums, <strong><em>Learn To Live</em></strong> and <strong><em>Charleston, SC 1966</em></strong> produced five No. 1 singles.  <strong><em>Southern Style</em></strong>, his fourth studio country album, features his latest No. 1 single &quot;Homegrown Honey&quot;. Rucker is currently headlining his <em>Good for a Good Time Tour</em>.  </p>
                    <p><br />
                    </p>
                    <p><strong>Hunter Hayes</strong></p>
                    <p>Hailed as a &quot;country-rock-blues guitar hero in the making&quot; by the <em>Los Angeles Times</em>, 5-time GRAMMY nominee Hunter Hayes is a singer, songwriter and multi-instrumentalist who roared onto the scene with his double-Platinum, self-titled debut album and then a No.1 sophomore album, <strong><em>Storyline</em></strong>, Hayes delved into unprecedented territory with the innovative rollout of new music via streaming and digital platforms in 2015, culminating in the release of a special, three-disc collection dubbed <strong><em>The 21 Project</em>.</strong> He's been working on his third full-length studio album</p>
                    <p><br />
                    </p>
                    <p><strong></strong><br />
                    </p>
                    <p><strong></strong><br />
                    </p>
                    <p><strong>Jennifer Nettles</strong></p>
                    <p>Grammy® Award-winning country superstar Jennifer Nettles recently released her second solo album <strong><em>Playing With Fire</em></strong>. Highlights include the stunning lead single &quot;Unlove You&quot; and a collaboration with Jennifer Lopez. During her career, including her time in the hit duo Sugarland, she has won five ASCAP awards and received a Grammy® Award, ACM Award and CMA Award for penning the emotionally charged, &quot;Stay.&quot; Nettles has performed with Beyoncé, Bon Jovi, John Legend, Katy Perry, Lady Gaga, Rihanna, and more. Her new Christmas album includes a collaborations with Idina Menzel. </p>
                    <p><strong></strong><br />
                    </p>
                    <p><strong></strong><br />
                    </p>
                    <p><strong>Maren Morris</strong></p>
                    <p>The 26 year-old Texan singer/songwriter has quickly established herself with vocal styling's that reflect her country, folk and pop influences. Armed with sheer talent, honest lyrics and a completely magnetic presence.  Morris' album, <strong><em>Hero</em></strong>, (which includes the huge hit singles &quot;My Church&quot; and &quot;80's Mercedes&quot;) entered the US country albums chart at No. 1. She has tied for the most nominations at the <em>50</em><em>th</em><em> Annual CMA Awards</em>, including Album of the Year, Female Vocalist of the Year, Single of the Year, Song of the Year and New Artist of the Year. </p>
                    <p><br />
                    </p>
                    <p><strong>Zac Brown Band</strong></p>
                    <p>Three-time GRAMMY winners and multi-platinum artists Zac Brown Band have become one of music's most heralded acts.  Their latest gold-certified album <strong><em>Jekyll + Hyde</em></strong>, marks the band's third consecutive #1 debut on the Billboard 200 chart. Together with their three platinum-selling albums, <strong><em>Uncaged</em></strong><strong>, </strong><strong><em>You Get What You Give</em></strong>, and <strong><em>The Foundation</em></strong>, and their 2013 project <strong><em>The Grohl Sessions</em></strong><em> </em><strong><em>Vol. 1</em></strong><strong>,</strong> the band has sold over eight million albums to date.  Zac Brown Band had been on the road headlining their &quot;Black Out The Sun&quot; tour throughout 2016. </p>
