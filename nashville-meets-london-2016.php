          <p align="center">To sample music from this year’s Nashville Meets London lineup, click here. </p>
          <p align="center">Canary Wharf is located in the former London Docklands. For the past ten years, Canary Wharf has hosted the Canary Wharf Jazz Festival, which is the largest free jazz festival in the UK, and has a long history of promoting arts and culture throughout its estate. </p>
          <p align="center">Nashville Meets London is co-produced by Canary Wharf Arts + Events, Peter Conway Management and Nashville-based agency, The AristoMedia Group.  Baylen Leonard, BBC Radio 2 Country DJ, is the artistic program consultant.</p>
