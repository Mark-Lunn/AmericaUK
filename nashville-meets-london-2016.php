          <p align="center">(Nashville, Tenn. – July 11, 2016) London-based company, Canary Wharf Group has announced the lineup for the inaugural Nashville Meets London music festival, scheduled for August 13-14 in Canada Square Park.  The lineup for the 2-day event will showcase established artists and exciting new talent, and includes four acts representing the United States and six acts representing the United Kingdom.  From the United States are American Young, Logan Brill, Ty Herndon, and Logan Mize, and representing the United Kingdom are Yola Carter, Megan O’Neill, Pauper Kings, Hannah Rose Platt, Honey Ryder, and William the Conqueror. The event is free and open to the public. </p>
          <p align="center">Performances will begin at 2 p.m. each day in Canada Square Park, located in the heart of Canary Wharf and surrounded by award-winning gardens and over 300 shops, bars and restaurants. The daily lineup is as follows:<br />
