                <p>Get ready for more adrenaline-pumping American football action this autumn. The National Football League recently unveiled the matchups for the 2019 London Games – and they’re everything a fan could dream of.</p>
                <p>Among the teams set to appear in the capital are the Los Angeles Rams. Last Sunday the Rams won a nail-biting NFC Championship game (see the highlights below) and they’re now heading to Super Bowl LIII on 3 February 2019. That means you’ll have the chance to see the NFL’s youngest head coach Sean McVay, along with fast-rising quarterback Jared Goff, star running back Todd Gurley and their fearsome offensive line in person! The Rams will take on the hungry Cincinnati Bengals, who are on the hunt to better their 2018 AFC North record.</p>
                <p>As part of the upcoming London Games, you can also see the AFC South champion Houston Texans face their division rival, the Jacksonville Jaguars. The Jags are set to play a home game here for their seventh consecutive season while the Texans will be making their UK debut.</p>
                <p>In addition, the Oakland Raiders (who are set to move to Las Vegas in 2020) will clash with the Chicago Bears. Plus the Tampa Bay Buccaneers will bring it on against the Carolina Panthers, another team making their UK debut.</p>
                <p>Two of the 2019 London Games will be held at Wembley Stadium and two will take places at the new 62,000-seat Tottenham Hotpsur Stadium. Dates, kickoff times and venues will be announced when the full NFL schedule is released this spring.</p>
                <p>With three sold-out games held at Wembley in consecutive weeks, the 2018 London Games were a huge success. Fans came out in record numbers. In fact, the final game between Jacksonville and Philadelphia saw a record attendance of 85,870 watch as the Super Bowl champion Eagles defeated the Jaguars 24-18. You can see highlights from that game below.</p>
                <p>Since 2007, London has hosted 24 regular season NFL games, with 29 of the 32 clubs making the trip from the US. In 2018, the Jacksonville Jaguars, Philadelphia Eagles, Los Angeles Chargers, Tennessee Titans, Seattle Seahawks and the Oakland Raiders battled it out in front of their UK fans.</p>
                <p>Before the 2018 games, we sent our team of Minimasters down to interview some of the star players from the teams. Watch how they got on below.</p>
                <p>London mayor Sadiq Khan said, “London is the world’s sporting capital and hosting NFL matches here is fantastic for fans of American football in the UK as well as supporters from all over Europe. The NFL brings huge benefits for our city and showcases London to millions of viewers overseas. We’ve staged some mouth-watering matches here and hope to host even more in the years to come as we demonstrate that London Is Open.”</p>
