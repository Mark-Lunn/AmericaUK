                  <p><strong>THE SHIRES</strong> &amp; <strong>WARD THOMAS</strong> 
                    <div align="center">
                    </p>
                  <p>SPEARHEAD NEW WAVE OF UK COUNTRY &amp;
                    ANNOUNCE CO-HEADLINE ‘HOME-GROWN TOUR’</p>
                </div>
                <em> “Even a few years ago, the idea of British Country music spelt wannabe Dollys, line-dancing seniors and unconvincing American accents. Now though, fresh artists with country influences and the dexterity to create a credible home-grown version of the music are emerging” – Sunday Times </em>
                  <div> </div>
                  Two fast-rising acts, The Shires &amp; Ward Thomas, on a joint tour that is set to be a landmark moment for the UK scene. The bands will take it in turns to headline on a series of dates around the UK. This is the moment fans of fresh and exciting country music have been waiting for!
                  <div> </div>
                  Country music in the UK has been having a renaissance in recent times, what with the success of the television series ‘Nashville’ and of the huge Country 2 Country event in London, which saw some of the biggest US names playing the UK. It isn’t just American talent that’s enjoying this resurgence though: The UK has produced a new wave of talent who are putting a British spin on the down south sounds of Nashville.
                  <div> </div>
                  <div>
                    <p>Chief among this new wave are <strong>The Shires </strong>and <strong>Ward Thomas</strong>, two bands from the Home Counties who have taken inspiration from the sounds of Nashville legends such as Johnny Cash and Dixie Chicks. Not content to merely copy the US acts though, these bands are flying the flag for a British Country music that reflects the lives of young people living in the UK. This shared vision has led the bands to team up and take their music on the road for the nationwide ‘Home-grown tour’.</p>
                    <p>&nbsp;</p>
                  </div>
                  
                  <div>
                    <p>Both bands have come a long way since they played the pop-up stages of C2C in March 2014, with both acts having an A-Listed track on BBC Radio 2 and The Shires an iTunes UK Country No.1 with their debut single Nashville Grey Skies, while Ward Thomas's debut album &quot;From Where We Stand&quot; went to No.1 on both the OCC Country Chart and iTunes' Country Chart.</p>
                    </div>
                  <div>
                    <p>The seeds for this tour were sown when the two bands ran into each other in a road house in Nashville while they were both paying pilgrimage to their spiritual home. The chance to take their shared message out on the road together seemed too good to miss and the ‘Home-grown tour’ was born. </p>
                    <p>&nbsp;</p>
                  </div>
                  <div>It seems that The Shires have come from nowhere overnight, but behind that first impression lies the story of two musicians who have been working towards this moment for a long time. Crissie has been singing her whole life, and came to love country music through artists like Dolly Parton and Faith hill, whereas Ben has toured as a singer/songwriter with the likes of K.T. Tunstall. Introduced by a mutual friend after a comment from Ben on Facebook, the pair instantly felt a musical connection, and discovered that they both lived near each other in “the Shires”. </div>
                  <p>&nbsp;</p>
                  <div>
                    <p>Hampshire and Country music may not be obvious bed fellows but 20-year old twin sisters Catherine and Lizzy Ward Thomas (who have been singing harmonies together since childhood) have had Country music in their blood ever since they were introduced to the sounds of Carrie Underwood, Dixie Chicks and Alison Krauss by a Canadian cousin in their early teens. One listen to the vocal cadences and close harmonies on their debut album ‘From Where We Stand’ and Nashville and Hampshire suddenly don’t seem all that far apart</p>
                    <p>&nbsp;</p>
